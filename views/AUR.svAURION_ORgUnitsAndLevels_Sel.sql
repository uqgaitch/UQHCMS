ALTER view [AUR].[svAURION_ORgUnitsAndLevels_Sel] as

SELECT T106F005_ORGANISATION_UNIT_NO, T106F020_ORGANISATION_UNIT_LVL, T911F015_DESCRIPTION
  FROM [DQS_STAGING_DATA].[AUR].[T106_ORGANISATION_UNITS] T106
  inner  join [AUR].[T911_ORG_UNIT_LEVELS] T911
  on t911.T911F005_ORG_UNIT_LEVEL = t106.T106F020_ORGANISATION_UNIT_LVL

  where T106F010_ORG_UNIT_STATUS = 'active'
GO
