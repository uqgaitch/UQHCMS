inner join AUR.svAURION_OrgUnitMaxESSLevel_Sel ESS on ESS.T101F015_ORGANISATION_UNIT_NO = T101.T101F015_ORGANISATION_UNIT_NO
and ESS.AUTH_LEVEL = T101.T101F235_ESS_AUTHORITY_LEVEL
left outer join AUR.svARUION_OrgUnitRelationship_Sel ORG on ORG.T106F005_ORGANISATION_UNIT_NO = T101.T101F015_ORGANISATION_UNIT_NO
